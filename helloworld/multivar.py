#Many Values to Multiple Variables
x, y, z = "Orange", "Apple", "Cherry"
print(x)
print(y)
print(z)

#One Value to Multiple Variables

x = y = z = "Orange"
print(x)
print(y)
print(z)

#unpack a collection

fruits = ["apple", "orange", "cherry"]
x, y, z = fruits
print(x)
print(y)
print(z)