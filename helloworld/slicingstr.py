b = "Hello, World!"
print(b[2:5])

#slice from the start

a = "Hello,world!"
print(a[:5])

#slice to the end
print(a[2:])

#negative indexing
print(b[-5:-2])

#uppercase
print(a.upper())

#lowercase
print(a.lower())

#remove whitespace
c = "    hello    "
print(c.strip())

#replace string
print(a.replace("H", "A"))

#split String
print(a.split(","))

#String Concatenation

x = "Hello"
y = "World"
z = x + y
print(z)
s = x + " " + y
print(s)
